// CommonJS Node build script (uses require + __dirname); run directly via node.
/**
 * Generate real OpenAI embeddings for test fixtures
 * Run: node scripts/generate-test-embeddings.js
 */

const fs = require('fs');
const path = require('path');

// Load environment variables
require('dotenv').config({ path: path.join(__dirname, '..', '.env') });

const OPENAI_API_KEY = process.env.OPENAI_API_KEY;
const OPENAI_EMBEDDINGS_URL = 'https://api.openai.com/v1/embeddings';
const EMBEDDING_MODEL = 'text-embedding-3-small';

if (!OPENAI_API_KEY) {
  console.error('Error: OPENAI_API_KEY not found in .env file');
  process.exit(1);
}

// All texts used in the test file that need embeddings
const TEST_TEXTS = {
  // Scene summaries
  sceneDiscoverDoor: 'Alice discovers a hidden door',
  sceneFindKey: 'Bob finds a mysterious key',
  sceneDecipherRunes: 'Carol deciphers ancient runes',
  sceneMagicalDoor: 'Alice discovers a hidden magical door',
  sceneAncientSword: 'Bob finds an ancient sword',

  // Propositions
  propDoorAncient: 'The door is ancient and ornate',
  propSymbols: 'Strange symbols cover the door frame',
  propInscription: 'The inscription is in an unknown language',
  propA: 'Proposition A',
  propB: 'Proposition B',
  propC: 'Proposition C',
  propGlowsEnergy: 'The door glows with arcane energy',
  propRustyKey: 'A rusty key hangs on the wall',

  // Prose
  proseDetailed: 'Alice pushed open the heavy wooden door. Beyond lay a corridor shrouded in darkness, its walls lined with ancient tapestries depicting forgotten battles.',
  proseSimple: 'Alice opened the door.',
  proseRewritten: 'With trembling hands, Alice slowly pushed the creaking door ajar.',

  // Search queries
  queryMagicalDiscovery: 'magical discovery',
  queryMagicalEnergy: 'magical energy',
  queryPowerfulMagic: 'powerful magic',

  // Test texts for batch operations
  testText: 'Test text',
};

async function generateEmbeddings(texts) {
  const response = await fetch(OPENAI_EMBEDDINGS_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${OPENAI_API_KEY}`,
    },
    body: JSON.stringify({
      model: EMBEDDING_MODEL,
      input: texts,
    }),
  });

  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`OpenAI API error: ${errorText}`);
  }

  const data = await response.json();
  return data.data.map(item => item.embedding);
}

async function main() {
  console.log('Generating real embeddings from OpenAI API...');
  console.log(`Model: ${EMBEDDING_MODEL}`);
  console.log(`Total texts: ${Object.keys(TEST_TEXTS).length}\n`);

  const textKeys = Object.keys(TEST_TEXTS);
  const textValues = Object.values(TEST_TEXTS);

  try {
    // Generate all embeddings in one batch
    const embeddings = await generateEmbeddings(textValues);

    console.log(`✓ Generated ${embeddings.length} embeddings`);
    console.log(`  Dimensions: ${embeddings[0].length}`);

    // Create TypeScript fixture code
    const fixtureCode = `// Real OpenAI embeddings for test fixtures
// Generated by scripts/generate-test-embeddings.js
// Model: ${EMBEDDING_MODEL}
// Generated: ${new Date().toISOString()}

export const TEST_EMBEDDINGS = {
${textKeys.map((key, i) => {
  const embedding = embeddings[i];
  return `  ${key}: ${JSON.stringify(embedding)},`;
}).join('\n')}
} as const;

// Helper to get embedding by key
export function getTestEmbedding(key: keyof typeof TEST_EMBEDDINGS): number[] {
  return TEST_EMBEDDINGS[key] as number[];
}
`;

    // Write to file
    const outputPath = path.join(__dirname, '..', 'src', '__tests__', 'fixtures', 'test-embeddings.ts');
    const outputDir = path.dirname(outputPath);

    // Create fixtures directory if it doesn't exist
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }

    fs.writeFileSync(outputPath, fixtureCode);

    console.log(`\n✓ Fixtures written to: ${outputPath}`);
    console.log('\nYou can now import these embeddings in your tests:');
    console.log("  import { TEST_EMBEDDINGS, getTestEmbedding } from './fixtures/test-embeddings';");

  } catch (error) {
    console.error('Error generating embeddings:', error.message);
    process.exit(1);
  }
}

main();
